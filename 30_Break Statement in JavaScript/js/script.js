// Demonstration of the 'break' statement in JavaScript

// A for loop that iterates from 1 to 10
for (let i = 1; i <= 10; i++) {
    console.log(i); // Print the current value of 'i'

    // Check if 'i' is equal to 4
    if (i == 4) {
        break; // Exit the loop when 'i' reaches 4
    }
}

// The loop stops executing once 'i' reaches 4, so numbers 1 to 4 are printed.

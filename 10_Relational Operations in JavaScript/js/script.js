// Relational Operations in JavaScript

/*
>  greater than
<  less than
>= greater than or equal to
<= less than or equal to
*/

// Declare two variables a and b
let a = 10;
let b = 20;

// Check if a is greater than b
console.log("Greater            : ", a > b);  // false, because 10 is not greater than 20

// Check if a is less than b
console.log("Lesser             : ", a < b);  // true, because 10 is less than 20

// Check if a is greater than or equal to b
console.log("Greater than equal : ", a >= b);  // false, because 10 is not greater than or equal to 20

// Check if a is less than or equal to b
console.log("Lesser than equal  : ", a <= b);  // true, because 10 is less than or equal to 20
